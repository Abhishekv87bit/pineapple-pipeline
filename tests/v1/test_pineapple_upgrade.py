"""Tests for pineapple_upgrade.py -- template version comparison and upgrade detection.

All tests use tmp_path fixtures and have no real filesystem dependencies.
"""

from pineapple_upgrade import (
    TemplateStatus,
    UpgradeReport,
    _extract_version,
    check_templates,
    show_diff,
    TEMPLATE_DESTINATIONS,
)


# ---------------------------------------------------------------------------
# _extract_version
# ---------------------------------------------------------------------------


class TestExtractVersion:
    def test_extract_standard_header(self):
        text = "# Pineapple Pipeline v1.2.3\nsome content"
        assert _extract_version(text) == "1.2.3"

    def test_extract_from_comment(self):
        text = "# Generated by Pineapple Pipeline v2.0.0\n"
        assert _extract_version(text) == "2.0.0"

    def test_extract_no_version(self):
        text = "just some file content without version"
        assert _extract_version(text) == ""

    def test_extract_multidigit(self):
        text = "Pineapple Pipeline v10.20.300"
        assert _extract_version(text) == "10.20.300"

    def test_extract_embedded_in_text(self):
        text = "This file was created by Pineapple Pipeline v1.0.0 on 2025-01-01"
        assert _extract_version(text) == "1.0.0"


# ---------------------------------------------------------------------------
# UpgradeReport
# ---------------------------------------------------------------------------


class TestUpgradeReport:
    def test_outdated_count_zero(self):
        report = UpgradeReport(
            templates=[
                TemplateStatus("t1", "f1", "current"),
                TemplateStatus("t2", "f2", "missing"),
            ]
        )
        assert report.outdated_count == 0

    def test_outdated_count_mixed(self):
        report = UpgradeReport(
            templates=[
                TemplateStatus("t1", "f1", "current"),
                TemplateStatus("t2", "f2", "outdated"),
                TemplateStatus("t3", "f3", "outdated"),
            ]
        )
        assert report.outdated_count == 2

    def test_to_dict_structure(self):
        report = UpgradeReport(
            templates=[
                TemplateStatus("tpl.py", "backend/tpl.py", "outdated",
                               current_version="2.0.0", project_version="1.0.0",
                               diff_lines=10),
            ]
        )
        d = report.to_dict()
        assert d["total_templates"] == 1
        assert d["outdated"] == 1
        assert d["templates"][0]["name"] == "tpl.py"
        assert d["templates"][0]["status"] == "outdated"
        assert d["templates"][0]["diff_lines"] == 10


