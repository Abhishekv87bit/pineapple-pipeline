class TestShowDiff:
    def test_diff_between_versions(self, tmp_path, monkeypatch):
        """Diff should show differences between project and template."""
        templates_dir = tmp_path / "templates"
        templates_dir.mkdir()
        (templates_dir / "rate_limiter.py").write_text(
            "# Pineapple Pipeline v2.0.0\nnew line\n", encoding="utf-8"
        )
        monkeypatch.setattr("pineapple_upgrade.TEMPLATE_DIR", templates_dir)

        project_dir = tmp_path / "project"
        dest = project_dir / "backend" / "app" / "middleware" / "rate_limiter.py"
        dest.parent.mkdir(parents=True)
        dest.write_text("# Pineapple Pipeline v1.0.0\nold line\n", encoding="utf-8")

        diff = show_diff(project_dir, "rate_limiter.py")
        assert "---" in diff
        assert "+++" in diff
        assert "v1.0.0" in diff
        assert "v2.0.0" in diff

    def test_diff_unknown_template(self, tmp_path):
        result = show_diff(tmp_path, "nonexistent_template.py")
        assert "Unknown template" in result

    def test_diff_missing_project_file(self, tmp_path, monkeypatch):
        templates_dir = tmp_path / "templates"
        templates_dir.mkdir()
        (templates_dir / "rate_limiter.py").write_text("content", encoding="utf-8")
        monkeypatch.setattr("pineapple_upgrade.TEMPLATE_DIR", templates_dir)

        result = show_diff(tmp_path, "rate_limiter.py")
        assert "not found" in result.lower()

    def test_diff_missing_template_file(self, tmp_path, monkeypatch):
        templates_dir = tmp_path / "templates"
        templates_dir.mkdir()
        # Template file doesn't exist
        monkeypatch.setattr("pineapple_upgrade.TEMPLATE_DIR", templates_dir)

        project_dir = tmp_path / "project"
        dest = project_dir / "backend" / "app" / "middleware" / "rate_limiter.py"
        dest.parent.mkdir(parents=True)
        dest.write_text("content", encoding="utf-8")

        result = show_diff(project_dir, "rate_limiter.py")
        assert "not found" in result.lower()
