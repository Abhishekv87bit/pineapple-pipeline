class TestCheckTemplates:
    def _setup_template_and_project(self, tmp_path, template_name, template_text,
                                     project_text=None, backend="backend", frontend="frontend"):
        """Helper: set up a template file and optionally a project file."""
        # Create the template directory relative to the tool
        # We need to monkeypatch TEMPLATE_DIR, but check_templates reads
        # from TEMPLATE_DIR which is module-level. Instead, we create the
        # structure the function expects and monkeypatch.
        templates_dir = tmp_path / "templates"
        templates_dir.mkdir(exist_ok=True)
        (templates_dir / template_name).write_text(template_text, encoding="utf-8")

        project_dir = tmp_path / "project"
        project_dir.mkdir(exist_ok=True)
        (project_dir / backend).mkdir(exist_ok=True)
        (project_dir / frontend).mkdir(exist_ok=True)

        if project_text is not None:
            dest_pattern = TEMPLATE_DESTINATIONS[template_name]
            dest_rel = dest_pattern.format(backend=backend, frontend=frontend)
            dest_file = project_dir / dest_rel
            dest_file.parent.mkdir(parents=True, exist_ok=True)
            dest_file.write_text(project_text, encoding="utf-8")

        return templates_dir, project_dir

    def test_missing_template_detected(self, tmp_path, monkeypatch):
        """File not in project -> status 'missing'."""
        templates_dir = tmp_path / "templates"
        templates_dir.mkdir()
        (templates_dir / "rate_limiter.py").write_text(
            "# Pineapple Pipeline v1.0.0\ncode", encoding="utf-8"
        )
        monkeypatch.setattr("pineapple_upgrade.TEMPLATE_DIR", templates_dir)

        project_dir = tmp_path / "project"
        project_dir.mkdir()

        report = check_templates(project_dir)
        rl = [t for t in report.templates if t.template_name == "rate_limiter.py"]
        assert len(rl) == 1
        assert rl[0].status == "missing"
        assert rl[0].current_version == "1.0.0"

    def test_current_version_detected(self, tmp_path, monkeypatch):
        """Same version in template and project -> status 'current'."""
        templates_dir = tmp_path / "templates"
        templates_dir.mkdir()
        content = "# Pineapple Pipeline v1.0.0\nrate limiter code"
        (templates_dir / "rate_limiter.py").write_text(content, encoding="utf-8")
        monkeypatch.setattr("pineapple_upgrade.TEMPLATE_DIR", templates_dir)

        project_dir = tmp_path / "project"
        dest = project_dir / "backend" / "app" / "middleware" / "rate_limiter.py"
        dest.parent.mkdir(parents=True)
        dest.write_text(content, encoding="utf-8")

        report = check_templates(project_dir)
        rl = [t for t in report.templates if t.template_name == "rate_limiter.py"]
        assert len(rl) == 1
        assert rl[0].status == "current"
        assert rl[0].current_version == "1.0.0"
        assert rl[0].project_version == "1.0.0"

    def test_outdated_version_detected(self, tmp_path, monkeypatch):
        """Old version in project, new in template -> status 'outdated'."""
        templates_dir = tmp_path / "templates"
        templates_dir.mkdir()
        (templates_dir / "rate_limiter.py").write_text(
            "# Pineapple Pipeline v2.0.0\nnew code\nextra line", encoding="utf-8"
        )
        monkeypatch.setattr("pineapple_upgrade.TEMPLATE_DIR", templates_dir)

        project_dir = tmp_path / "project"
        dest = project_dir / "backend" / "app" / "middleware" / "rate_limiter.py"
        dest.parent.mkdir(parents=True)
        dest.write_text("# Pineapple Pipeline v1.0.0\nold code", encoding="utf-8")

        report = check_templates(project_dir)
        rl = [t for t in report.templates if t.template_name == "rate_limiter.py"]
        assert len(rl) == 1
        assert rl[0].status == "outdated"
        assert rl[0].current_version == "2.0.0"
        assert rl[0].project_version == "1.0.0"
        assert rl[0].diff_lines > 0

    def test_custom_file_detected(self, tmp_path, monkeypatch):
        """Project file with no version header -> status 'custom'."""
        templates_dir = tmp_path / "templates"
        templates_dir.mkdir()
        (templates_dir / "rate_limiter.py").write_text(
            "# Pineapple Pipeline v1.0.0\ntemplate code", encoding="utf-8"
        )
        monkeypatch.setattr("pineapple_upgrade.TEMPLATE_DIR", templates_dir)

        project_dir = tmp_path / "project"
        dest = project_dir / "backend" / "app" / "middleware" / "rate_limiter.py"
        dest.parent.mkdir(parents=True)
        dest.write_text("# My custom rate limiter\nno version header here", encoding="utf-8")

        report = check_templates(project_dir)
        rl = [t for t in report.templates if t.template_name == "rate_limiter.py"]
        assert len(rl) == 1
        assert rl[0].status == "custom"

    def test_auto_detect_backend_dir(self, tmp_path, monkeypatch):
        """Should detect 'server' as backend if 'backend' dir doesn't exist."""
        templates_dir = tmp_path / "templates"
        templates_dir.mkdir()
        (templates_dir / "rate_limiter.py").write_text(
            "# Pineapple Pipeline v1.0.0\ncode", encoding="utf-8"
        )
        monkeypatch.setattr("pineapple_upgrade.TEMPLATE_DIR", templates_dir)

        project_dir = tmp_path / "project"
        project_dir.mkdir()
        (project_dir / "server").mkdir()
        dest = project_dir / "server" / "app" / "middleware" / "rate_limiter.py"
        dest.parent.mkdir(parents=True)
        dest.write_text("# Pineapple Pipeline v1.0.0\ncode", encoding="utf-8")

        report = check_templates(project_dir)
        rl = [t for t in report.templates if t.template_name == "rate_limiter.py"]
        assert len(rl) == 1
        assert rl[0].status == "current"
        assert "server/" in rl[0].project_file


